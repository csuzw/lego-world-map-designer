var selectedColor = "";
var leftMouseButtonOnlyDown = false;

function initLegoWorldMapDesigner() {
    initMap(0);
    initEvents();
}

function initMap(mapType) {
    $(".cell").remove();

    var counter = 0;
    while (counter < 10240) {
        // 128 x 80
        let x = (counter + mapType) % 128;
        let y = (counter / 128) >> 0;
        let colorIndex =
            y < world.length && x < world[y].length ? world[y][x] : 0;
        let color = colors[colorIndex];
        let type = colorIndex === 1 || colorIndex === 2 ? "land" : "water";
        $("#canvas").append(
            `<div class='cell ${color}' data-x='${x}' data-y='${y}' data-color='${color}' data-type='${type}'></div>`
        );
        counter++;
    }

    $(".cell").click(function () {
        if (!selectedColor) return;
        if (isCellUnlocked($(this))) {
            $(this).attr("class", `cell ${selectedColor}`);
            setCounters();
        }
    });

    $(".cell").mouseover(function () {
        if (!leftMouseButtonOnlyDown || !selectedColor) return;
        if (isCellUnlocked($(this))) {
            $(this).attr("class", `cell ${selectedColor}`);
            setCounters();
        }
    });
    setCounters();
}

function initEvents() {
    document.body.onmousedown = setLeftButtonState;
    document.body.onmousemove = setLeftButtonState;
    document.body.onmouseup = setLeftButtonState;

    $(".color-button").click(function () {
        setSelectedColor($(this).attr("id"), $(this));
    });

    $("#clear").click(function () {
        setSelectedColor("");
        $(".cell").each(function () {
            let color = $(this).data("color");
            $(this).attr("class", `cell ${color}`);
        });
        setCounters();
    });

    $("#fill").click(function () {
        if (!selectedColor) return;
        $(".cell").each(function () {
            if (isCellUnlocked($(this))) {
                $(this).attr("class", `cell ${selectedColor}`);
            }
        });
        setCounters();
    });

    $("#download").click(function () {
        html2canvas($("#canvas")[0]).then(function (canvas) {
            let image = canvas
                .toDataURL("image/png")
                .replace("image/png", "image/octet-stream");
            let link = document.createElement("a");
            link.download = "lego-world-map.png";
            link.href = image;
            link.click();
        });
    });

    $("#map-counter-toggle").click(function () {
        $(".map-counter").attr("hidden", function (_, attr) {
            return !attr;
        });
        if ($(this).text() === "HIDE") {
            $(this).text("SHOW");
        } else {
            $(this).text("HIDE");
        }
    });

    $("#map-type").change(function () {
        let mapType = parseInt($(this).val());
        initMap(mapType);
    });
}

function setLeftButtonState(e) {
    leftMouseButtonOnlyDown =
        e.buttons === undefined ? e.which === 1 : e.buttons === 1;
}

function isCellUnlocked(element) {
    let type = element.data("type");
    isLandLocked = $("#lock-land").is(":checked");
    isWaterLocked = $("#lock-water").is(":checked");
    return (
        (!isWaterLocked && type === "water") ||
        (!isLandLocked && type === "land")
    );
}

function setSelectedColor(colorClass, element) {
    selectedColor = colorClass;
    $("#map-controls").children(".color-button").removeClass("selected");
    if (selectedColor) {
        element.addClass("selected");
        $("#fill").attr("class", `action-button ${colorClass}`);
    } else {
        $("#fill").attr("class", "action-button");
    }
}

function setCounters() {
    $(".map-counter").each(function () {
        let color = $(this).data("color");
        let max = $(this).data("max");
        let count = $(`.cell.${color}`).length;
        $(this).html(`${count}/${max}`);
    });
}

